# String
# False '' en cadena vacia
# True para cadena con la menos un caracter
cadena_vacia = ''
valor = bool(cadena_vacia)
print(f'valor de cadena_vacia: {cadena_vacia}, valor: {valor}') # False

cadena_con_valor = 'Hola'
valor = bool(cadena_con_valor)
print(f'valor de cadena_con_valor: {cadena_con_valor}, valor: {valor}') # True
